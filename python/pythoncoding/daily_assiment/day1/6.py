grade = input("Enter a grade in A B C D E F ")
if(grade=='A'):
    print('Excellent')
elif(grade=='B'):
    print('Good')
elif(grade=='C'):
    print('Average')
elif(grade=='D'):
    print('Below Average')
elif(grade=='E'):
    print('Poor')
elif(grade=='F'):
    print('Fail')
else:
    print('Invalid Input')